<p>17 июля 2009 года на территории курорта «Игора» состоялась конференция на тему: <strong>«<a name="top_conf_2009"></a>Внутрикорпоративные коммуникации: для кого, для чего, расходы или инвестиции».</strong></p>
<p>На конференции были рассмотрены такие темы как стоимость и отдача от использования Интранет-пораталов и внутренних печатных изданий, целевые группы персонала и средства корпоративных коммуникаций. Участники конференции обменивались личным опытом и навыками в этих областях.</p>
<p>В рамках конференции прошел конкурс печатных корпоративных изданий, который вызвал немалый интерес. В ходе борьбы между газетами-участниками, по итогам конкурса был выявлен победитель, которым стала газета компании «Усть-Луга». По словам президента Клуба Валерия Владимировича Куприна «Клуб «Кочубей» до настоящего момента не проводил конференций, конференция 17 июля была первой, и с уверенностью можно сказать, что она прошла успешно. Тема оказалась очень актуальной для специалистов сферы HR и вызвала неподдельный интерес участников».</p>
<p>Петербургский профессиональный кадровый клуб «Кочубей» благодарит всех принявших участие в работе конференции.</p>
