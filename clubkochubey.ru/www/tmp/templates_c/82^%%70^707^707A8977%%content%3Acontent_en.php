<h3><strong><a name="Manager_conf_2009"></a>Организатор конференции</strong></h3>
<p>Петербургский профессиональный кадровый клуб «Кочубей» основан 14 октября 1998 года и с тех пор объединяет высших менеджеров и специалистов в области управления персоналом, руководителей и собственников компаний Санкт-Петербурга.</p>
<p>Миссия Клуба - развитие области управления человеческими ресурсами через общение и развитие профессионалов в области управления персоналом, обмен идеями, методиками и наработками.</p>
<p>Политикой Клуба является высокий профессиональный уровень проводимых мероприятий и приглашаемых докладчиков, а также привлечение самых уважаемых и известных HR специалистов, топ-менеджеров и собственников бизнеса Санкт-Петербурга.</p>
