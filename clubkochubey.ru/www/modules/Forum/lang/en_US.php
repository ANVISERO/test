<h3>What Does This Do?</h3>
<p>This <strike>is</strike><sup>will be</sup> a simple message board with multiple threaded forums.<br /></p>

<h3>Some Feature</h3>
Forum categories with sections (via TreeManager module)<br />
Authentication, properties and avatar support (via FrontEndUsers module)<br />
Search (via Search module)<br />
Report to moderators (via CMSMailer module)<br />
Captcha (via Captcha module)<br />
Akismet (via Akismet module)<br />
RSS (via CGFeedMaker module)<br />
All templates in DB and customizable<br />
CMSMS events OnNewTopic, OnEditPost and OnReplyPost<br />
BBcode<br />
Ranks support<br />
All parameters are "per forum"<br />
Private forum, moderate forum (on approve from moderator) and public forum (where guest can read only or writing also)<br />
Banned members and/or IP<br />
Moderators: delete topic/post, sticky/closed topic, editing post, move topic, approve topic/post<br />

<h3>How Do I Use It</h3>
<p>Users need the "Modify Forum" permission in order to access the admin panel and "Modify Templates" permission in order to change frontend templates.<br />
From here one may create new forums, manage existing ones with own properties, change global settings, manage banned users and ip and manage frontend display and editing templates.<br />
Now many properties are <b>per forum</b>, for example you can setting different member group for each forum and/or setting this forum for allow guest write (create topic and post) to guest user, etc...</p>
<p>For public forum you must set allow guest in property of forum (allow guest write for create topic and post but no editing). My advice is to set ALWAYS moderator group for editing/delete topic and post.<br />
Optional parameter forum select which forums display, in this case you can display a few forums in a page and other forums in other page (statistcs user are common).</p>
<p>Insert the forum in the front end with {cms_module module="Forum"}<br /></p>

<h3>BBCode</h3>
<p>You may use BBCode in your posts. Enable for default in Settings tab.<br /></p>

<h3>Support for showing newest post on any selected page</h3>
<p>Use action="last_posts" in Forum tag for display last posts. Use last_posts smarty template for presentation. My advice is to set the forum alias page (global but good for Search module too). You can use forum attribute for limit forum display, p.e. forum="4,6" display last posts of forum ID 4 and 6 only.<br /></p>

<h3>RSS</h3>
<p>Install CGExtensions and CGFeedMaker modules. In CGFeedMaker add a new feed (ex: forum) and in template use:</p>
<pre>{* original rss feed template *}
&lt;?xml version="1.0"?&gt;
&lt;rss version="2.0" xmlns:atom="http://www.w3.org/2005/Atom"&gt;
  &lt;channel&gt;
  {* note: if you have not configured pretty urls or mod rewrite, the next line may fail when trying to validate the feed *}
  &lt;atom:link href="{$feed_url}" rel="self" type="application/rss+xml" /&gt;
	&lt;title&gt;{$feed.title}&lt;/title&gt;
	{if isset($feed.link) && !empty($feed.link)}&lt;link&gt;{$feed.link}&lt;/link&gt;{/if}
	{if isset($feed.description) && !empty($feed.description)}&lt;description&gt;{$feed.description}&lt;/description&gt;{/if}
	{if isset($feed.copyright) && !empty($feed.copyright)}&lt;copyright&gt;{$feed.copyright}&lt;/copyright&gt;{/if}
	{if isset($generator)}&lt;generator&gt;{$generator}&lt;/generator&gt;{/if}
	{if isset($feed.managing_editor) && !empty($feed.managing_editor)}&lt;managingEditor&gt;{$feed.managing_editor}&lt;/managingEditor&gt;{/if}
	{if isset($admin_email)}&lt;webMaster&gt;{$admin_email} ({$admin_user->firstname} {$admin_user->lastname})&lt;/webMaster&gt;{/if}
	{if isset($feed.image) && !empty($feed.image)}
	  &lt;image&gt;
	{if isset($feed.description) && !empty($feed.description)}&lt;description&gt;{$feed.description}&lt;/description&gt;{/if}
		{if isset($feed.link) && !empty($feed.link)}&lt;link&gt;{$feed.link}&lt;/link&gt;{/if}
		&lt;title&gt;{$feed.title}&lt;/title&gt;
		&lt;url&gt;{$file_location}/{$feed.image}&lt;/url&gt;
		&lt;/image&gt;
	{/if}

	{* an example of how to create a feed from a call to the news module *}
	{* you can use any smarty variable that is available in the news summary template *}
	{* you can substitute this logic with output from any module that supports a summary
	   view, or possibly get artistic and mix output from different modules *}
{cms_module module="Forum" action="last_posts" assign="junk"}
{if $itemcount > 0}
	{foreach from=$items item=entry}
	&lt;item&gt;
	  &lt;category&gt;{$entry->forum_name|cms_html_entity_decode}&lt;/category&gt;
	  &lt;title&gt;{$entry->subject|cms_html_entity_decode}&lt;/title&gt;
	  &lt;link&gt;{$entry->url}#msg{$entry->post_id}&lt;/link&gt;
	  &lt;description&gt;{$entry->body|trim|strip_tags|summarize:40}&lt;/description&gt;
	  &lt;pubDate&gt;{$entry->last_poster_time|rfc_date}&lt;/pubDate&gt;
	  &lt;guid&gt;{$entry->url}#msg{$entry->post_id}&lt;/guid&gt;
	&lt;/item&gt;
	{/foreach}
{/if}
  &lt;/channel&gt;
&lt;/rss&gt;</pre>
<p>In your page or teplate use: {CGFeedMaker action="rsslink" feed="forum"}<br /></p>

