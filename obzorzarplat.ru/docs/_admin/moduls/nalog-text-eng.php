<center>
<strong><em>Стандартные налоговые вычеты.</em></strong>
</center>
<br><br>
Кроме социальных и имущественных налоговых вычетов по НДФЛ, Вы имеете право на получение стандартных налоговых вычетов.
<br><br>
Эти вычеты можно разделить на две группы:
<ol>
<li>Вычеты первой группы - это необлагаемый минимум ваших доходов. В этих целях Вам предоставляется вычет в размере 400 руб. ежемесячно до тех пор, пока ваш доход нарастающим итогом с начала года не превысит 20 000 руб. Определенные категории налогоплательщиков имеют право на вычет в размере 500 руб. или 3000 руб. ежемесячно в течение года (без ограничения максимальной суммы дохода).
<br><br>
Иногда налогоплательщик имеет право более чем на один из перечисленных вычетов. Тогда ему предоставляется максимальный из них.<br><br>

<li>Ко второй группе относятся вычеты в виде расходов на обеспечение детей. Такой вычет предоставляется на каждого ребенка в возрасте до 18 лет, а также на каждого учащегося дневной формы обучения - аспиранта, ординатора, студента, курсанта в возрасте до 24 лет. Вычет в размере 600 руб. полагается до тех пор, пока доход нарастающим итогом с начала года не превысит 40 000 руб.

</ol>
Если Вы не относитесь к категории налогоплательщиков, имеющих право на вычеты в размере 500 руб. или 3000 руб. ежемесячно, то сумма, возвращаемая Вам из бюджета составит <strong><?=round($deduction_stand);?> руб. </strong>
<br><br>
Также Вы можете получить вычеты в виде расходов на обеспечение детей в размере <strong><?=round($deduction_stand_baby);?> руб. </strong>
