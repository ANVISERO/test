В течение всего года вы уплачиваете налог на доходы физических лиц. Этот налог удерживает работодатель из вашей зарплаты и перечисляет его в бюджет.<br><br>
Таким образом, сумма НДФЛ, уплаченная вами в бюджет РФ за <b><?=$year_now;?></b> год составит <b><?=round($tax);?> руб.</b> 
<hr>

Если вы хотите получить налоговые вычеты, то с наступлением следующего года необходимо подать в налоговую инспекцию по месту жительства декларацию о доходах. При этом необходимо предъявить документы, доказывающие расходы, которые вы собираетесь применять в качестве налоговых вычетов.
<br><br>
Налоговый орган произведет перерасчет налога на добавленную стоимость (НДФЛ) с учетом предъявленных прав на вычеты. Излишне уплаченный налог вам вернут путем перечисления на ваш счет.
 <br><br>
Обратите внимание, что вернуть Вам могут только те деньги, которые были уплачены Вами в бюджет за прошлый год (за исключением имущественных налоговых вычетов, которые могут быть перенесены на последующие годы).
 <br><br>
<center>
<strong><em>Социальные налоговые вычеты.</em></strong>
</center>
<br><br>
К социальным налоговым вычетам в соответствии со ст.219 НК РФ относятся:
<ul>
<li>Сумма доходов, перечисленная на благотворительные цели;<br><br>
<li>Сумма расходов на свое обучение (не более 50 000 руб. в год);<br><br>
<li>Сумма расходов на обучение своего ребенка, которому еще не исполнилось 24 лет (не более 50 000 руб. на каждого ребенка в общей сумме на обоих родителей в год);<br><br>
<li>Сумма расходов на свое лечение и лечение своих родственников (супруги, родители, дети до 18 лет) (не более 50 000 руб. год, за исключением, видов медицинской помощи, по которым вся сумма без ограничения принимается к вычету).
</ul>
Таким образом, вы сможете уменьшить налогооблагаемую базу и вернуть из бюджета следующие суммы:
<ul>
<li><b><?=round($deduction_welfair);?> руб.</b> – при перечислении денег на благотворительные цели;<br><br>
<li><b><?=round($deduction_education);?> руб.</b> – если вы оплачивали свое обучение;<br><br>
<li><b><?=round($deduction_education_baby);?> руб.</b> – если вы оплачивали обучение своего ребенка;<br><br>
<li><b><?=round($deduction_treatment);?> руб.</b> – если вы оплачивали лечение в этом году;<br><br>
<li><b><?=round($deduction_treatment_dear);?> руб.</b> – если вы оплачивали дорогостоящее лечение или приобретали лекарственные препараты, по которым вся сумма без ограничения принимается к вычету.
</ul>
Если вы использовали все указанные виды социальных налоговых вычетов, общая сумма, которая будет вам выплачена из бюджета составит <b><?=round($deduction_social);?> руб.</b>
 <br><br>
<center>
<strong><em>Имущественные налоговые вычеты.</em></strong>
</center>
<br><br>
Если Вы <strong><em>приобрели</em></strong> жилой дом,  квартиру или долю в них, то Вы вправе вернуть часть потраченных денег (возврат подоходного налога), заявив  об имущественном налоговом вычете. 
<br><br>
При этом могут быть учтены следующие расходы:
<ul>
<li>сумма, израсходованная Вами на новое строительство либо приобретение на территории Российской Федерации жилого дома, квартиры или доли (долей) в них, в размере фактически произведенных расходов;<br><br>
<li>сумма, направленная на погашение процентов по целевым займам (кредитам), полученным от кредитных и иных организаций Российской Федерации и фактически израсходованным им на новое строительство либо приобретение на территории Российской Федерации жилого дома, квартиры или доли (долей) в них.
</ul>
Общий размер указанного имущественного налогового вычета, не может превышать 1000000 руб. Этот вычет может быть получен только один раз. Если в налоговом периоде (один год) он не может быть использован полностью, то остаток суммы вычета переносится на последующие периоды до полного ее использования.
<ul>
<li><b><?=round($deduction_house);?> руб.</b> – сумма, которую Вы можете вернуть из бюджета.<br><br>
<li><b><?=round($years);?></b> – количество лет, за которые Вам будет выплачена вся сумма.
</ul>
Также Вы имеете право на имущественный налоговый вычет при получении дохода от <strong><em>продажи</em></strong> квартиры, жилого дома, включая приватизированные жилые помещения, и долей в указанном имуществе. 
