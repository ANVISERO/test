    предоставляются налогоплательщику одним из налоговых агентов, являющихся источником выплаты дохода, по его выбору на основании 
    письменного заявления и документов, подтверждающих право на такие вычеты:</p>
<ul>
    <li>в размере <b>3000 руб.</b> (чернобыльцы, инвалиды ВОВ, инвалиды из числа военнослужащих 1,2 и 3 групп);</li>
    <li>в размере <b>500 руб.</b> (Герои СССР, Герои России, награжденные орденом Славы 3 степеней, участники ВОВ, инвалиды с детства, инвалиды 1 и 2 групп и др.);</li>
    <li>в размере <b>400 руб.</b> для остальных лиц, предоставляется до суммы дохода 40 000 руб.;</li>
    <li>в размере <b>1000 руб.</b> на каждого ребенка, предоставляется до суммы дохода 280 000 руб.</li>
