<h2><a class="lk_h2" name="4">Профессиональные налоговые вычеты</a></h2>
<p>Профессиональные налоговые вычеты установлены <a href="http://www.consultant.ru/popular/nalog2/3_3.html#p3566" class="lk2">статьей 221 НК РФ</a>.</p>
<p>Право на получение профессиональных налоговых вычетов имеют:</p>
<ul>
<li>предприниматели,</li>
<li>нотариусы, занимающиеся частной практикой,</li>
<li>адвокаты, учредившие адвокатские кабинеты,</li>
<li>иные лица, занимающиеся частной практикой.</li>
</ul>
<p>Профессиональные налоговые вычеты предоставляются в сумме фактически произведенных и документально подтвержденных расходов, непосредственно
    связанных с извлечением доходов.</p>
<p>При этом состав указанных расходов, принимаемых к вычету, определяется налогоплательщиком самостоятельно в порядке, аналогичном порядку
    определения расходов для целей налогообложения, установленному главой "Налог на прибыль организаций". К указанным расходам налогоплательщика
    относится также государственная пошлина, которая уплачена в связи с его профессиональной деятельностью.</p>
