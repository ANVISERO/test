<div class="body">
<p>Ежегодный основной оплачиваемый отпуск составляет <strong>28 календарных дней</strong>.</p>
<p>В стаж работы, дающий право на ежегодный основной оплачиваемый отпуск, включаются:</p>
<ul>
<li>время фактической работы;</li>
<li>время, когда Вы фактически не работали, но за Вами сохранялось место работы (должность), в том числе время
