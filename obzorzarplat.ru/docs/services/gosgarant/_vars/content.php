    на прежней работе;</li>
<li>период отстранения от работы, если Вы не прошли обязательный медицинский осмотр (обследование) не по своей 
    вине.</li>
</ul>
<p>Право на использование отпуска за первый год работы возникает у Вас по истечении шести месяцев непрерывной
    работы у данного работодателя. По соглашению сторон оплачиваемый отпуск может быть предоставлен и до
