<h2><a class="h3-click">Ваши гарантии и компенсации, связанные со служебной командировкой</a></h2>
<div class="body">
    <p>Если Вас направили в служебную командировку, то за Вами в установленном порядке сохраняются место
        работы (должность), а также средний заработок.</p>
    <p>Кроме того, Вам должны быть возмещены следующие <b>командировочные расходы</b>:</p>
      <ul>
  <li>расходы по проезду;</li>
  <li>расходы по найму жилого помещения;</li>
  <li>дополнительные расходы, связанные с проживанием вне места постоянного жительства (суточные);</li>
  <li>иные расходы, произведенные работником с разрешения или ведома работодателя.</li>
</ul>
    <br>
<p>К <b>прочим расходам</b> можно отнести:</p>
<ul>
  <li>расходы по оплате услуг связи;</li>
  <li>дополнительные услуги, связанные с наймом жилья (в том числе бронирование номера);</li>
  <li>расходы на получение и регистрацию заграничного паспорта;</li>
  <li>расходы на получение визы;</li>
  <li>сборы за услуги аэропортов;</li>
  <li>комиссионные сборы;</li>
  <li>расходы, связанные с обменом наличной валюты или чека в банке на наличную иностранную валюту.
</ul>
<br>
<p>Нормы суточных, <b>не облагаемые НДФЛ:</b><br>
<strong>700 руб./день</strong> – при командировке по России;<br>
<strong>2500 руб./день</strong> – при командировке заграницу. </p>
