<h2><a class="h3-click">Ваши гарантии и компенсации при расторжении трудового договора</a></h2>
<div class="body">
<p>Если трудовой договор был расторгнут в связи с:</p>
<ul>
<li>ликвидацией организации,</li>
<li>сокращением численности или штата работников организации,</li>
</ul>
<p>то Вам выплачивается выходное пособие в размере среднего месячного заработка, а также за Вами сохраняется
    <b>средний месячный заработок на период трудоустройства</b>, но не свыше двух месяцев со дня увольнения (с
    зачетом выходного пособия).</p>
<p>Если трудовой договор был расторгнут в связи с:</p>
<ul>
<li>отказом от перевода на другую работу, необходимую Вам в соответствии с медицинским заключением или 
    отсутствием соответствующей работу у работодателя,</li>
<li>призывом на военную службу или направлением на заменяющую ее альтернативную гражданскую службу,</li>
<li>восстановлением на работе работника, ранее выполнявшего эту работу,</li>
<li>вашим отказом от перевода на работу в другую местность вместе с работодателем,</li>
<li>признанием Вас полностью неспособным к трудовой деятельности в соответствии с медицинским заключением,</li>
<li>вашим отказом от продолжения работы в связи с изменением определенных сторонами условий трудового договора,</li>
</ul>
<p>то Вам полагается выходное пособие в размере двухнедельного среднего заработка.</p>
<p>При сокращении численности или штата работников преимущественное право на оставление на работе
    предоставляется работникам с более высокой производительностью труда и квалификацией.</p>
<li>При равной производительности труда и квалификации предпочтение в оставлении на работе отдается:</li>
<ul>
<li>семейным – при наличии двух или более иждивенцев (нетрудоспособных членов семьи, находящихся на полном
    содержании работника или получающих от него помощь, которая является для них постоянным и основным
    источником средств к существованию);</li>
<li>лицам, в семье которых нет других работников с самостоятельным заработком; работникам, получившим в период 
    работы у данного работодателя трудовое увечье или профессиональное заболевание;</li>
<li>инвалидам Великой Отечественной войны и инвалидам боевых действий по защите Отечества;</li>
<li>работникам, повышающим свою квалификацию по направлению работодателя без отрыва от работы.</li>
</ul>
<br>
<p>При проведении мероприятий по сокращению численности или штата работников организации работодатель обязан
    предложить Вам другую имеющуюся работу (вакантную должность).</p>
<p>О предстоящем увольнении в связи с ликвидацией организации, сокращением численности или штата работников
    организации работники предупреждаются работодателем персонально и под роспись не менее чем за 2 месяца до
    увольнения.</p>
<p>Если Вы являетесь руководителем организации, его заместителем или главным бухгалтером, то в случае
    расторжения трудового договора в связи со сменой собственника имущества организации новый собственник
    обязан выплатить Вам компенсацию в размере не ниже трех средних месячных заработков.</p>
